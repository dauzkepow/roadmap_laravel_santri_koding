//Variabel & Tipe Data
//Fungsi = Menyimpan data agar bisa digunakan/dimodifikasi.

/*
var → lama (function scope, kurang direkomendasikan).
let → bisa diubah nilainya (block scope).
const → nilai tetap, tidak bisa diubah.
*/

//Contoh Kode
var nama = "Daus";
let umur = 31;
const negara = "Indonesia";

console.log(nama, umur, negara); //Daus 31 Indonesia
//Penjelasan baris code
/*
var nama = "Dauz"; → menyimpan string "Dauz".
let umur = 25; → menyimpan angka.
const negara = "Indonesia"; → nilai tetap.
console.log(...) → menampilkan variabel.
*/

